	public static String generateUrl(String host, String urlPath,
			String[] params, String[] values) {
		if (host == null || params != null && values != null
				&& params.length != values.length)
			throw new RuntimeException(
					"host equal null, or UrlHelpers params length not equal values length");
		StringBuilder url = new StringBuilder();
		url.append(host);
		url.append(urlPath);

		for (int i = 0; i < params.length; i++) {
			if (i == 0) {
				url.append("?");
			}
			url.append(params[i]);
			url.append("=");
			url.append(values[i]);
			if (i == params.length - 1) {
				break;
			}
			url.append("&");
		}
		String urlStr = url.toString().replaceAll("&&", "&")
				.replaceAll("\\?\\?", "\\" + "?");
		LogUtil.e("UrlHelpers", "url=========>>>>" + urlStr);
		return urlStr;
	}

